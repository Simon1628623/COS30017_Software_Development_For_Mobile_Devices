public class Bike
{
    public String Make;
    public Integer Year;
    public String Model;
    public Double Odometer;

    public Bike()
    {

    }

    public Bike(String m, Integer y, String mod, Double Odo)
    {
        Make = m;
        Year = y;
        Model = mod;
        Odometer = Odo;
    }
}
